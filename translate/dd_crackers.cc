void 
piraq_crackers (char *srs, char *dst, int item_count, int ndx_cnt, int *tbl, int srs_ndx, int dst_ndx, int limit)
{
   int ii, jj, itm, nn, *row = tbl;
   char *ss=srs, *tt=dst;

   for(itm=0; itm < item_count;) {

      switch (row[id_ndx]) {

       case type_char:		/* just copy it */
	 memcpy(tt, ss, row[rpt_ndx]);
	 break;

       case type_short:
	 memcpy(tt, ss, row[rpt_ndx] * sizeof(short));
	 break;

       case type_long:
       case type_float:
       case type_int:
	 memcpy(tt, ss, row[rpt_ndx] * sizeof(int32_t));
	 break;

       case type_double:	/* same as unpacking two floats? */
	 memcpy(tt, ss, row[rpt_ndx] * sizeof(double));
	 break;

       default:
	 break;

      }
      itm += row[skp_ndx];
      row = tbl + (itm * ndx_cnt);
      ss = srs + row[srs_ndx];
      tt = dst + row[dst_ndx];
   }
}
