{
   for(; nn--;) {
      *tt++ = *(ss+7);
      *tt++ = *(ss+6);
      *tt++ = *(ss+5);
      *tt++ = *(ss+4);
      *tt++ = *(ss+3);
      *tt++ = *(ss+2);
      *tt++ = *(ss+1);
      *tt++ = *ss;
      ss += 8;
   }
}
