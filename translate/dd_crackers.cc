void 
crackers (char *srs, char *dst, const int item_count, const int ndx_cnt, int *tbl, int srs_ndx, int dst_ndx, int limit)
{
   int ii, jj, itm, nn, *row = tbl;
   char *ss=srs, *tt=dst;
   char *deep6 = 0;


   for(itm=0; itm < item_count;) {

      switch(row[id_ndx]) {

       case type_char:		/* just copy it */
	 memcpy(tt, ss, row[rpt_ndx]);
	 break;

       case type_short:

	    swack_short(ss, tt, row[rpt_ndx]);
	 break;

       case type_long:
       case type_float:
       case type_int:

	    swack_long(ss, tt, row[rpt_ndx]);
	 break;

       case type_double:
	 swack_double(ss, tt, row[rpt_ndx]);
	 break;

       default:
	 *deep6 = 0;
	 break;

      }
      itm += row[skp_ndx];
      row = tbl + (itm * ndx_cnt);
      ss = srs + row[srs_ndx];
      tt = dst + row[dst_ndx];
   }
}
